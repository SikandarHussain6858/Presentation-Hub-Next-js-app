import mongoose from 'mongoose';

const FeedbackSchema = new mongoose.Schema({
    rating: {
        type: Number,
        required: true,
        min: 1,
        max: 5
    },
    comment: {
        type: String,
        required: true,
        maxlength: 500
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

export default mongoose.models.Feedback || mongoose.model('Feedback', FeedbackSchema);
